    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String accountNo = txtAccountNo.getText();
        String accountName = txtAccountName.getText();
        Date openDate = jDateOpenAccount.getDate();
        String cashLimit = txtCashLimit.getText();
        String creditLimit = txtCreditLimit.getText();
        boolean result = false;
        if (!cashLimit.isEmpty()) {
            if (normalSpendAccount != null) {
                normalSpendAccount.setAccountNo(accountNo);
                normalSpendAccount.setAccountName(accountName);
                normalSpendAccount.setOpenDate(openDate);
                normalSpendAccount.setCashLimit(new BigDecimal(cashLimit));
                result = accountController.update(normalSpendAccount, oldAccountNo);
            } else if (vipSpendAccount != null) {
                vipSpendAccount.setAccountNo(accountNo);
                vipSpendAccount.setAccountName(accountName);
                vipSpendAccount.setOpenDate(openDate);
                vipSpendAccount.setCashLimit(new BigDecimal(cashLimit));
                result = accountController.update(vipSpendAccount, oldAccountNo);
            }
        } else if (!creditLimit.isEmpty()) {
            if (creditAccount != null) {
                creditAccount.setAccountNo(accountNo);
                creditAccount.setAccountName(accountName);
                creditAccount.setOpenDate(openDate);
                creditAccount.setCreditLimit(new BigDecimal(creditLimit));
                result = accountController.update(creditAccount, oldAccountNo);
            }
        }
        if (!result) {
            JOptionPane.showMessageDialog(this, "Update Account không thành công");
            return;
        }
        JOptionPane.showMessageDialog(this, "Update Account thành công");
        List<Account> accounts = accountController.getAllAccounts();
        if (accounts == null) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra...");
            return;
        }
        this.accountForm.setAccountList(accounts);
        this.setVisible(false);
        this.accountForm.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

