    void setData(Account account) {
        this.oldAccountNo = account.getAccountNo();
        txtAccountNo.setText(account.getAccountNo());
        txtAccountName.setText(account.getAccountName());
        jDateOpenAccount.setDate(account.getOpenDate());
        String cashLimit = "";
        String creditLimit = "";
        if (account instanceof NormalSpendAccount) {
            NormalSpendAccount normalSpendAccount = (NormalSpendAccount) account;
            this.normalSpendAccount = normalSpendAccount;
            cashLimit = Utils.customCurrency("###,###.##", String.valueOf(normalSpendAccount.getCashLimit()));
            txtCreditLimit.setEnabled(false);
            txtCashLimit.setEditable(true);
        } else if (account instanceof VipSpendAccount) {
            VipSpendAccount vipSpendAccount = (VipSpendAccount) account;
            this.vipSpendAccount = vipSpendAccount;
            cashLimit = Utils.customCurrency("###,###.##", String.valueOf(vipSpendAccount.getCashLimit()));
            txtCreditLimit.setEnabled(false);
            txtCashLimit.setEditable(true);
        } else {
            CreditAccount creditAccount = (CreditAccount) account;
            this.creditAccount = creditAccount;
            creditLimit = Utils.customCurrency("###,###.##", String.valueOf(creditAccount.getCreditLimit()));
            txtCashLimit.setEnabled(false);
            txtCreditLimit.setEditable(true);
        }
        txtCashLimit.setText(cashLimit);
        txtCreditLimit.setText(creditLimit);
    }

